    @Override
    public boolean getDiagonal() {
        boolean activada = rbtnDiagonal.isSelected();
        if((int) spCantColumnas.getValue() == (int) spCantFilas.getValue() && activada){
            return true;
        }
        else if(activada && (int) spCantFilas.getValue() != (int) spCantColumnas.getValue()){
            JOptionPane.showMessageDialog(this,"No se puede activar Diagonal", "Error", JOptionPane.ERROR_MESSAGE); 
            rbtnDiagonal.setSelected(false);
            return false;
        } 
        return activada;
    }
    

    @Override
    public boolean getCentro() {
        boolean activada = rbtnCentro.isSelected();
        if((int) spCantColumnas.getValue() == (int) spCantFilas.getValue() && activada 
                && (int) spCantFilas.getValue() %2 != 0 && (int) spCantColumnas.getValue() %2 != 0){
            return true;
        }
        else if(activada){
            JOptionPane.showMessageDialog(this,"No se puede activar Centro", "Error", JOptionPane.ERROR_MESSAGE); 
            rbtnCentro.setSelected(false);
            return false;
        } 
        return activada;
    }

