    private void btnGuardarConfiguracionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarConfiguracionActionPerformed
        try {
            if(getCantFilas() < 1 || getCantFilas() > 11 ||
                 getCantColumnas() < 0 || getCantColumnas() > 11 ||
                    getCantJugadores() < 2 || getCantJugadores() > 3 ||
                    getCartonesPorJugador() < 0 || getValorCarton() < 0 || getTiempoEspera() < 10){
                 JOptionPane.showMessageDialog(this,"Configuración no válida","Error",JOptionPane.ERROR_MESSAGE);     
            }
            else if(controller.partidaActiva()){
                JOptionPane.showMessageDialog(this,"No se puede modificar la configuración","Hay un juego activo",JOptionPane.ERROR_MESSAGE); 
            }
            else{
                controller.guardarConfiguracion();            
                JOptionPane.showMessageDialog(this, "Configuración guardada");      
                controller.listarConfiguraciones();
                controller.cargarBotonesFiguras();
            } 
        } catch (RemoteException ex) {
            Logger.getLogger(FrmConfiguracion.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGuardarConfiguracionActionPerformed

